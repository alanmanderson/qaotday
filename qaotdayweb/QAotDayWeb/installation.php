<h2>Installation</h2>
<ol style="list-style-type: decimal;">
<li>  Go to <a href="http://fastqs.com">fastqs.com<a/> via the browser on your phone.</li>
<li>  Click on the link called "fastqs.apk".  This will download the file and save it with your downloads.</li>
<li>  Once the file downloads open the fastqs.apk file and install fastqs.
	<ol style="list-style-type: lower-alpha">
		<li>  If you can't find the file do the following:</li>
		<li>  Open the Browser, open the options menu, click "More", then "Downloads"</li>
		<li>  find fastqs which will probably be at the bottom.</li>
	</ol>
</li>
</ol>
<h2>Configuration</h2>
<ol>
<li>  Go to your applications menu and scroll down to "Q & A of the Day"</li>
<li>	You will be taken to the registration page.  Username and Password are the only required fields.</li>
<li>  Now that you are registered you will need to register for a topic before you will receive any questions.
	<ol style="list-style-type: lower-alpha">
		<li>  Click on "Register for a new topic" (You can subscribe and unsubscribe with this button)</li>
	</ol>
</li>
<li>  You might want to check if the daily question has been sent out in any of the topics you subscribe to.
	<ol style="list-style-type: lower-alpha">
		<li>  To do so, click on Answer today's question.  Your subscirbed topics appear.</li>
		<li>	If the daily question in any of your topics have been sent for the day the "Get Question" button will be enabled.</li>
		<li>  Otherwise, it will not be enabled and you can hit "Return to Main Menu"</li>
	</ol>
</li>
</ol>
	
Note:  The app is called Q&A of the Day, and the web page is called fastqs.com (fast questions).  That is where the names come from.

Thank you for your time and we hope you enjoy!
